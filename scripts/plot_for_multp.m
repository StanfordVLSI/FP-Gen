    fclose(fid);    
      
    % extract data from cell array C
    BoothType = data{strmatch('top_MultiplierP.MultiplierP.BoothType', headers)};
    ModifiedBooth = data{strmatch('top_MultiplierP.MultiplierP.ModifiedBooth', headers)};

