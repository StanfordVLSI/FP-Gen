% Generate four graphs for MultiplierP data
% Note: the second argument 'bitwidth' is just four graph titles and output
% filename
function plot_for_multp(filename, bitwidth)

    fid = fopen(filename);
    data = {};
    headers = {};
    
    % read the headers
